SELECT COURSES.COURSENO AS COURSENO, COURSES.COURSENAME AS COURSENAME,
      COURSES.CREDITS AS CREDITS, COURSES.TOTAL as TOTAL,COURSES.HOURS AS HOURS, COURSES.LHOURS AS LHOURS,
      COURSES.EXPERIMENTS AS EXPERIMENTS, COURSES.COMPUTING AS COMPUTING,
      COURSES.SHOURS AS SHOURS, COURSES.KHOURS AS KHOURS, COURSES.ZHOURS AS ZHOURS,
      RTRIM(COURSES.SYLLABUS) AS SYLLABUS,
      SCHOOLS.NAME AS SNAME,RTRIM(COURSES.REM) REM,COURSES.OLDCOURSENO OLDCOURSENO,COURSES.SCHOOL SCHOOL,COURSES.TYPE TYPE,COURSES.TYPE2 TYPE2,
      RTRIM(courses.课程介绍) as INTRODUCE,RTRIM(ISNULL(quarter,'')) AS QUARTER,Limit
FROM COURSES INNER JOIN
      SCHOOLS ON COURSES.SCHOOL = SCHOOLS.SCHOOL INNER JOIN
      COURSETYPEOPTIONS ON COURSES.TYPE = COURSETYPEOPTIONS.NAME INNER JOIN
      COURSETYPEOPTIONS2 ON COURSES.TYPE2 = COURSETYPEOPTIONS2.NAME
left outer join (select count(*) amount,courseno from courseplan where courseplan.courseno=:idone
group by courseno)as temp on temp.courseno=courses.courseno
WHERE COURSES.COURSENO=:idtwo
ORDER BY COURSES.COURSENO