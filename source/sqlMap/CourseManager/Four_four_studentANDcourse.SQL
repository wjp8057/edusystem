SELECT STUDENTS.STUDENTNO,SELECTION.COURSENOGROUP,SELECTION.COURSETYPE
FROM STUDENTS LEFT OUTER JOIN (
SELECT R32.STUDENTNO,R32.COURSENO AS COURSENOGROUP,R32.COURSETYPE
FROM R32
WHERE R32.YEAR=:YEAR
AND R32.TERM=:TERM
)AS SELECTION
ON STUDENTS.STUDENTNO=SELECTION.STUDENTNO
WHERE STUDENTS.CLASSNO=:CLASSNO
ORDER BY STUDENTS.STUDENTNO