select count(*) as ROWS from(SELECT row_number() over(ORDER BY STUDENTS.SCHOOL,STUDENTS.STUDENTNO) as row,STUDENTS.STUDENTNO as xh,
STUDENTS.NAME AS xm,
STUDENTS.CLASSNO,
STUDENTS.SCHOOL,
SCHOOLS.NAME AS xy,
CLASSES.CLASSNAME as bj,
STUDENTS.POINTS as jdf,
0 AS REPEAT
FROM STUDENTS
LEFT OUTER JOIN CLASSES
ON STUDENTS.CLASSNO=CLASSES.CLASSNO
LEFT OUTER JOIN SCHOOLS
ON STUDENTS.SCHOOL=SCHOOLS.SCHOOL
WHERE STUDENTS.CLASSNO LIKE :CLASSNO and students.free=0
)as b