select * from(SELECT row_number() over(ORDER BY STUDENTS.STUDENTNO) as row,
STUDENTS.STUDENTNO as xh,
STUDENTS.NAME AS xm,
CLASSES.CLASSNAME as xsbj,
SELECTIONS.TOTALCREDITS as zxf,
SELECTIONS.TOTALCOURSES as xkms
FROM STUDENTS left outer JOIN CLASSES ON STUDENTS.CLASSNO=CLASSES.CLASSNO
LEFT OUTER JOIN
(
  SELECT STUDENTNO,SUM(COURSES.CREDITS) AS TOTALCREDITS,COUNT(*) AS TOTALCOURSES
  FROM R32
  JOIN COURSES ON R32.COURSENO=COURSES.COURSENO
  WHERE YEAR=:YEAR
  AND TERM=:TERM
  GROUP BY STUDENTNO
)AS SELECTIONS ON STUDENTS.STUDENTNO=SELECTIONS.STUDENTNO
WHERE STUDENTS.STUDENTNO like :STUDENTNO
) as b where b.row between :start and :end