SELECT SCHEDULE.COURSENO+SCHEDULE.[GROUP] AS COURSENOGROUP,CLASSROOMS.SEATS
FROM SCHEDULE JOIN CLASSROOMS ON SCHEDULE.ROOMNO=CLASSROOMS.ROOMNO
WHERE SCHEDULE.YEAR=:YEAR
AND SCHEDULE.TERM=:TERM
AND CLASSROOMS.EQUIPMENT IN ('M','E','L','U','S','Y')
AND CLASSROOMS.[USAGE] = 'P'
AND CLASSROOMS.ROOMNO<>'000000000'
ORDER BY COURSENOGROUP
