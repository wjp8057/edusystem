select count(*) from(SELECT row_number() over(order by R30.MAJORPLAN_ROWID) as row,R30.MAJORPLAN_ROWID, R30.PROGNO, 教学计划类别代码.value, R30.CREDITS,
      R30.MCREDITS, MAJORCODE.NAME, PROGRAMS.PROGNAME,教学计划类别代码.NAME AS TYPE
FROM R30 INNER JOIN
      教学计划类别代码 ON R30.计划类别 = 教学计划类别代码.name INNER JOIN
      MAJORPLAN ON R30.MAJORPLAN_ROWID = MAJORPLAN.ROWID INNER JOIN
      MAJORS ON MAJORPLAN.MAP = MAJORS.ROWID INNER JOIN
      MAJORCODE ON MAJORS.MAJORNO = MAJORCODE.CODE INNER JOIN
      PROGRAMS ON R30.PROGNO = PROGRAMS.PROGRAMNO
where R30.MAJORPLAN_ROWID=:rowid
) as b