SELECT PROGRAMS.PROGNAME  as jhm,
R30.PROGNO AS jhh,
R30.计划类别 jhlb,
R30.CREDITS AS jhxf,
 R30.MCREDITS AS jhbx,
 MAJORPLAN.CREDITS AS zxf,
 MAJORPLAN.MCREDITS AS zbx,
 rtrim(MAJORPLAN.专业方向)as zyfx,
 rtrim(MAJORCODE.NAME) AS zymc,
 MAJORPLAN.[YEAR] AS nj
 FROM  R30 INNER JOIN PROGRAMS ON PROGRAMS.PROGRAMNO=R30.PROGNO
 INNER JOIN MAJORPLAN ON MAJORPLAN.ROWID = R30.MAJORPLAN_ROWID
 INNER JOIN MAJORS ON MAJORS.ROWID = MAJORPLAN.MAP
 INNER JOIN  MAJORCODE ON MAJORCODE.CODE = MAJORS.MAJORNO WHERE MAJORPLAN_ROWID=:rowid order by jhlb
