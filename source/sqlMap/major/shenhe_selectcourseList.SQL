SELECT DISTINCT COURSES.COURSENO AS courseno,COURSES.COURSENAME as coursename,
CREDITS as credit  FROM R12 INNER JOIN COURSES ON COURSES.COURSENO=R12.COURSENO
 WHERE  R12.PROGRAMNO =:programno AND NOT EXISTS (SELECT * FROM SCORES WHERE STUDENTNO=:stone AND
R12.COURSENO=SCORES.COURSENO AND (EXAMSCORE>=60 OR EXAMSCORE2>=60 OR TESTSCORE IN ('优秀','良好','及格','中等','合格')
 OR TESTSCORE2 IN ('优秀','良好','及格','中等','合格')))
AND NOT EXISTS (SELECT * FROM SCORES INNER JOIN R33 ON R33.EQNO=SCORES.COURSENO
WHERE  R33.PROGRAMNO=R12.PROGRAMNO AND STUDENTNO=:sttwo AND R12.COURSENO=R33.COURSENO AND
(EXAMSCORE>=60 OR EXAMSCORE2>=60 OR TESTSCORE IN ('优秀','良好','及格','中等','合格')
OR TESTSCORE2 IN ('优秀','良好','及格','中等','合格')))
AND NOT EXISTS (SELECT * FROM SCORES INNER JOIN R33 ON R33.COURSENO=SCORES.COURSENO
WHERE R33.PROGRAMNO=R12.PROGRAMNO AND STUDENTNO=:stthree AND R12.COURSENO=R33.EQNO
AND  (EXAMSCORE>=60 OR EXAMSCORE2>=60 OR TESTSCORE IN ('优秀','良好','及格','中等','合格')
OR TESTSCORE2 IN ('优秀','良好','及格','中等','合格')))  ORDER BY courseno ;