SELECT STUDENTS.STUDENTNO,SCORES.COURSENO AS COURSENOGROUP,SCORES.PLANTYPE,
scores.TESTSCORE,scores.examscore as em
FROM STUDENTS inner join SCORES on scores.studentno=students.studentno
inner join EXAMREMOPTIONS on examremoptions.code=scores.examrem
WHERE SCORES.YEAR=:YEAR
  AND SCORES.TERM=:TERM
  AND STUDENTS.CLASSNO=:CLASSNO
ORDER BY STUDENTS.STUDENTNO