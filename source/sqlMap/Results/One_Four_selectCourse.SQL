SELECT DISTINCT(scores.COURSENO+scores.[GROUP]) AS COURSENOGROUP,COURSES.COURSENAME,CAST(COURSES.CREDITS AS CHAR) AS CREDITS,scores.PLANTYPE
FROM scores JOIN COURSES ON scores.COURSENO=COURSES.COURSENO
JOIN STUDENTS ON scores.STUDENTNO=STUDENTS.STUDENTNO
WHERE STUDENTS.CLASSNO=:CLASSNO
AND scores.YEAR=:YEAR
AND scores.TERM=:TERM
ORDER BY scores.PLANTYPE DESC,COURSENOGROUP

